//Basico
let numbers: number[] = [1, 2, 3, 4, 5]; // Declaración de un array de números
let names: string[] = ["Juan", "María", "Pedro"]; // Declaración de un array de strings


//intermedio
let matrix: number[][] = [[1, 2], [3, 4], [5, 6]]; // Declaración de una matriz (array de arrays)
let tuple: [string, number, boolean] = ["Juan", 25, true]; // Declaración de una tupla que combina diferentes tipos de datos


//Avanzado
let mixedArray: (string | number)[] = ["Hola", 10, "Mundo", 20]; // Declaración de un array que puede contener strings y números
let readonlyTuple: readonly [string, number] = ["Juan", 25]; // Declaración de una tupla de solo lectura

