function greet(name: string): void {
  console.log(`Hola, ${name}!`); // Imprime un saludo utilizando el nombre pasado como argumento
}

greet("Juan"); // Llama a la función 'greet' pasando el argumento "Juan". Imprime: "Hola, Juan!"

// En resumen, este código define una función llamada greet que recibe un parámetro name de tipo
// string y no devuelve nada. Dentro de la función, se imprime un saludo utilizando el nombre
//  pasado como argumento. Luego, se llama a la función greet pasando el argumento "Juan", 
//  lo cual imprime el saludo "Hola, Juan!".