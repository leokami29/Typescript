let funcoo = function (name: string): void {
  console.log(`Hola, ${name}!`); // Imprime un saludo utilizando el nombre pasado como argumento
};

funcoo("Juan"); // Llama a la función 'funcoo' pasando el argumento "Juan". Imprime: "Hola, Juan!"


// En resumen, este código define una función anónima y la asigna a la variable funcoo. 
// La función anónima recibe un parámetro name de tipo string y no devuelve nada. 
// Dentro de la función, se imprime un saludo utilizando el nombre pasado como argumento. 
// Luego, se llama a la función funcoo pasando el argumento "Juan", lo cual imprime el saludo "Hola, Juan!".