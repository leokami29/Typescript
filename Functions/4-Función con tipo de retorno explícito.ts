function addNumbers(a: number, b: number): number {
  return a + b; // Devuelve la suma de los dos números proporcionados como argumentos
}

let result: number = addNumbers(5, 3); // Llama a la función 'addNumbers' pasando los números 5 y 3 como argumentos. El resultado se asigna a la variable 'result'. El valor de 'result' será 8.


